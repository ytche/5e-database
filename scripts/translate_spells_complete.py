def translate_spell(spell):
    """Translate a single spell using manual translations."""
    name_en = spell['name']
    
    # Skip if not in our translation list
    if name_en not in SPELL_TRANSLATIONS:
        return spell
    
    translation = SPELL_TRANSLATIONS[name_en]
    
    # Save original values
    if 'name_en' not in spell:
        spell['name_en'] = name_en
    if 'desc_en' not in spell:
        spell['desc_en'] = spell['desc'].copy() if isinstance(spell['desc'], list) else spell['desc']
    if 'higher_level' in spell and spell['higher_level'] and 'higher_level_en' not in spell:
        spell['higher_level_en'] = spell['higher_level'].copy()
    if 'material' in spell and spell['material'] and 'material_en' not in spell:
        spell['material_en'] = spell['material']
    
    # Apply translations
    if 'name' in translation:
        spell['name'] = translation['name']
    if 'desc' in translation:
        spell['desc'] = translation['desc']
    if 'higher_level' in translation and 'higher_level' in spell:
        spell['higher_level'] = translation['higher_level']
    if 'material' in translation and 'material' in spell:
        spell['material'] = translation['material']
    if 'range' in translation:
        spell['range'] = translation['range']
    if 'duration' in translation:
        spell['duration'] = translation['duration']
    if 'casting_time' in translation:
        spell['casting_time'] = translation['casting_time']
    
    return spell

def main():
    # Read input
    with open('src/2014-zh/5e-SRD-Spells.json', 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Count and translate spells
    low_level_spells = [s for s in data if s['level'] <= 2]
    print(f"Found {len(low_level_spells)} spells with level 0-2")
    
    # Check which spells have translations
    missing = [s['name'] for s in low_level_spells if s['name'] not in SPELL_TRANSLATIONS]
    if missing:
        print(f"WARNING: {len(missing)} spells missing translations:")
        for name in missing[:10]:
            print(f"  - {name}")
        if len(missing) > 10:
            print(f"  ... and {len(missing) - 10} more")
    
    translated_count = 0
    for spell in data:
        if spell['level'] <= 2:
            translate_spell(spell)
            translated_count += 1
    
    # Write output
    with open('src/2014-zh/5e-SRD-Spells.json', 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f"Translated {translated_count} spells")
    print(f"Translations available for {len(SPELL_TRANSLATIONS)} spells")
    return 0

if __name__ == '__main__':
    sys.exit(main())
